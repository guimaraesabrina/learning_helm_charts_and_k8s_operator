
# simbolos utilizados nas URLs das requisições: 

    # '?' = usado para separar a parte da rota da parte dos parâmetros de consulta em uma URL.

    # '&' = usado para separar múltiplos parâmetros de consulta em uma única URL. por exemplo, /search?query=fastapi&limit=10 tem dois parâmetros de consulta: query com valor "fastapi" e limit com valor 10.

    # '/' = usado para separar partes de uma rota em uma URL. por exemplo, em /users/123, "users" é a parte da rota que especifica o recurso e "123" é um identificador exclusivo para esse recurso.

    # '.' = usado para separar o nome de um arquivo de sua extensão em uma URL. por exemplo, em example.com/index.html, "index" é o nome do arquivo e "html" é a extensão.

    # '#' = usado para referenciar uma âncora em uma página HTML. por exemplo, em example.com/page.html#section, "section" é o nome da âncora que deve ser exibida na página

